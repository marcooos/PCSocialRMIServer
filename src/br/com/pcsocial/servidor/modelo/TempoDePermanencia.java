package br.com.pcsocial.servidor.modelo;

public class TempoDePermanencia {
	private String descMercado;
	private long diasHospedados;
	
	public String getDescMercado() {
		return descMercado;
	}
	public void setDescMercado(String descMercado) {
		this.descMercado = descMercado;
	}
	public long getDiasHospedados() {
		return diasHospedados;
	}
	public void setDiasHospedados(long diasHospedados) {
		this.diasHospedados = diasHospedados;
	}

	
}
